# custom_int.s — Project 3 中断演示小程序
#
# 工作方式:
#   1. 初始化基址寄存器，把 x10/x14 清零，然后写 0xD000_0000 使能中断
#   2. 主循环不断把 x10++ 并刷到 7 段数码管（地址 0xE000_0000）
#   3. 按下 BTN_out[1] 触发硬件中断，CPU 跳到 0x80 的 ISR
#   4. ISR 把 x14++ 并写到 LED（0xF000_0000），然后 sw 到 0xD000_0008 完成 MRET
#
# 寄存器约定:
#   x10 — 主循环计数器（数码管 data0 通道）
#   x11 — 0xE000_0000  数码管基址
#   x12 — 0xD000_0000  CSR 基址
#   x14 — 中断计数器（LED 显示）
#   x15 — 0xF000_0000  LED/GPIO 基址
#   x0  — 始终 0（用作 sw 的"数据无关"占位）

# ============================================================
#  主程序入口 (PC = 0x00)
# ============================================================
0x00:  lui   x11, 0xE0000        # 0xE00005B7
0x04:  lui   x12, 0xD0000        # 0xD0000637
0x08:  lui   x15, 0xF0000        # 0xF00007B7
0x0C:  addi  x10, x0, 0          # 0x00000513    main counter = 0
0x10:  addi  x14, x0, 0          # 0x00000713    ISR  counter = 0
0x14:  sw    x0,  0(x12)         # 0x00062023    mie ← 1 (使能中断)

# 主循环 (PC = 0x18)
0x18:  addi  x10, x10, 1         # 0x00150513    counter++
0x1C:  sw    x10, 0(x11)         # 0x00A5A023    数码管显示 counter
0x20:  jal   x0,  -8             # 0xFF9FF06F    跳回 0x18

# 0x24..0x7C: 全部填 NOP (0x00000013)

# ============================================================
#  中断服务例程 (PC = 0x80, 由 MTVEC 硬编码指定)
# ============================================================
0x80:  addi  x14, x14, 1         # 0x00170713    ISR 计数器++
0x84:  sw    x14, 0(x15)         # 0x00E7A023    LED 显示 ISR cnt
0x88:  sw    x0,  8(x12)         # 0x00062423    MRET (PC←mepc, mie=1)

# 0x8C+: NOP 填充。ISR 必须以 MRET 结束，否则会落入 NOP 区。
